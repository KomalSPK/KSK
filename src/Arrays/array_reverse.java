package Arrays;

public class array_reverse {
	
	public static void main(String[] args) {
		int k[] = new int[6];
		k[0]=100;
		k[1]=200;
		k[2]=300;
		k[3]=400;
		k[4]=500;
		k[5]=600;
		System.out.println("Reverse array is as follows -");
		for (int i=5; i>=0; i--)
		{
		System.out.println(k[i]);
		}
	}

}
